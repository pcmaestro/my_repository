#Operadores de comparación

a = 5

b = 10

igual = (a == b) # En este caso igual valdrá True si a y b son iguales

print("Valor de igual : " + str(igual))

# !=  Este operador equiale a no igualdad

distinto = (a != b)

print("Valor de distinto : " + str(distinto))

# > mayor que,   < menor que

mayor_que = a > b

print("Valor de mayor_que : " + str(mayor_que))

menor_que = a < b

print("Valor de menor_que : " + str(menor_que))

#<= menor o igual que,  >= mayor o igual que

mayor_o_igual_que = a >= b

print("Valor de mayor_o_igual_que : " + str(mayor_o_igual_que))

menor_o_igual_que = a <= b

print("Valor de menor_o_igual_que : " + str(menor_o_igual_que))

